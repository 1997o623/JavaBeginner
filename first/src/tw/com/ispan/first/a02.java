package tw.com.ispan.first;

public class a02 {

	public static void main(String[] args) {

		// 變數 => 型別 =>基本 / 物件
		// 整數 / 浮點數 / 字元 /布林值 => 8大基本型別
		// 變數命名[a-zA-Z$_]都可以放第一個字
		// [a-zA-Z0-9$_]可以放第二個字之後
		byte n01 = 123; // 2^8 =>-128~127(0算正的)
		// 宣告變數順便給值
		System.out.println(n01);
		short n02 = 12345; // 2^16 => -32768~32767
		int n03; // 2^32 =>-21億~21億(4G)
		long n04; // 2^64
	}

}
